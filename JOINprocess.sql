SELECT DISTINCT top 100  H.NUM_PROCES, M.EMPNOME, COUNT(H.NUM_PROCES) AS SOLICITACOES FROM DOCUMENTO AS D 
         JOIN ANEXO_PROCES AS A ON A.NUM_SEQ_MOVTO_ORIG = D.VERSAO_ATIVA
         JOIN ML001855 AS M ON D.NR_DOCUMENTO = M.DOCUMENTID
         JOIN histor_proces AS H ON H.NUM_PROCES = A.NUM_PROCES  
         JOIN PROCES_WORKFLOW as P on H.NUM_PROCES = P.NUM_PROCES
         WHERE  P.[STATUS] = 0 GROUP by M.EMPNOME,h.NUM_PROCES ORDER BY 1
         

